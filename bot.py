# ========== РАСШИРЕННЫЙ ПОИСК (С ИСПРАВЛЕНИЕМ ОШИБОК) ==========
def search_articles(query: str):
    """Ищет статьи, возвращает список подходящих (несколько вариантов)"""
    query_lower = query.lower().strip()
    found = []
    
    # 1. Поиск по номеру статьи
    match_num = re.search(r'(\d{1,2}\.\d{1,2}(?:\.\d{1})?)', query_lower)
    if match_num:
        art_num = match_num.group(1)
        for law in laws:
            if law["article"] == art_num:
                return [law]
    
    # 2. Поиск по ключевым словам с группировкой (несколько статей)
    keywords_map = {
        "убийство": ["6.2", "6.3"],
        "побои": ["6.1"],
        "угроза": ["6.4"],
        "похищение": ["7.1"],
        "клевета": ["7.4", "7.4.1"],
        "изнасилование": ["8.1"],
        "кража": ["10.1"],
        "мошенничество": ["10.2"],
        "вымогательство": ["10.2.1"],
        "грабеж": ["10.3"],
        "разбой": ["10.4"],
        "угон": ["10.5", "10.5.1"],
        "уничтожение": ["10.6", "10.7", "10.8"],
        "проникновение": ["10.9", "12.7", "12.7.1", "12.7.2"],
        "налог": ["11.3", "11.4"],
        "терроризм": ["12.1", "12.1.1"],
        "оружие": ["12.8", "12.8.1", "12.8.2", "12.9", "12.10"],
        "наркотик": ["13.1", "13.2", "13.3", "13.4", "13.5"],
        "взятка": ["15.4", "15.5", "15.5.1"],
        "халатность": ["15.6"],
        "ложный": ["16.8", "16.13", "16.13.1", "16.16"],
        "побег": ["16.15"],
        "насилие": ["17.1", "17.2"],
        "оскорбление": ["17.3", "17.9"],
        "неповиновение": ["17.6"],
        "браконьерство": ["19.1"],
    }
    
    for word, article_nums in keywords_map.items():
        if word in query_lower:
            for art_num in article_nums:
                for law in laws:
                    if law["article"] == art_num and law not in found:
                        found.append(law)
            if found:
                return found
    
    # 3. Поиск по любому совпадению в названии
    for law in laws:
        if law["title"].lower() in query_lower or query_lower in law["title"].lower():
            found.append(law)
    
    # 4. Если ничего не найдено — исправляем опечатки
    if not found:
        all_titles = [law["title"].lower() for law in laws]
        matches = get_close_matches(query_lower, all_titles, n=3, cutoff=0.6)
        if matches:
            for match in matches:
                for law in laws:
                    if law["title"].lower() == match and law not in found:
                        found.append(law)
    
    return found

